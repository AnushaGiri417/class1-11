package com.class09;

public class NumberPattern_K_Format {

	public static void main(String[] args) {
		
		
		for(int a=7;a>=2;a--) {
			for(int b=1;b<=a;b++) {
				System.out.print(b+" ");
			}
			System.out.println();
		}
		
		for(int c=1;c<=7;c++) {
			for(int d=1;d<=c;d++) {
				System.out.print(d+" ");
			}
		System.out.println();
	
		}	   

		
		
	}

}
